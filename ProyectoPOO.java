public class LinkedListRemoveExample {
    public static void main(String[] args) {
        LinkedList<String> linkedList = new LinkedList<>();

        // Agregar elementos a la LinkedList
        linkedList.add("A");
        linkedList.add("B");
        linkedList.add("C");
        linkedList.add("D");
        linkedList.add("E");

        // Imprimir la LinkedList original
        System.out.println("Original LinkedList: " + linkedList);

        // Eliminar el elemento en el medio (índice 2)
        linkedList.remove(2);

        // Imprimir la LinkedList después de la eliminación
        System.out.println("LinkedList después de eliminar en el medio: " + linkedList);
    }
}

  
